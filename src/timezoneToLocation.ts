export default function getLocationFromTimezone(timezoneName: string) {
    if (!(timezoneName in TIMEZONE_LOCATION_MAP)) {
        throw new Error('Cannot detect location based on timezone. Please set location manually.')
    }

    const [ latitude, longitude ] = (TIMEZONE_LOCATION_MAP as unknown as {[key: string]: [number, number]})[timezoneName]
    return { latitude, longitude }
}
